    private void excluirClienteSelecionado() {
    int linhaSelecionada = jtableClientes.getSelectedRow();

    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jtableClientes.getModel();
    
    // **ASSUNÇÃO CRÍTICA**: O CPF está na primeira coluna (índice 0)
    String cpfCliente = (String) model.getValueAt(linhaSelecionada, 0); 

    int confirmacao = JOptionPane.showConfirmDialog(this, 
            "Tem certeza que deseja excluir o cliente com CPF: " + cpfCliente + "?", 
            "Confirmação de Exclusão", 
            JOptionPane.YES_NO_OPTION);

    if (confirmacao == JOptionPane.YES_OPTION) {
        try {
            clienteDAO.excluir(cpfCliente);
            JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            
            // Recarrega a lista para remover a linha excluída da JTable
            carregarDadosClientes();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                    "Erro ao excluir cliente: " + ex.getMessage(), 
                    "Erro de Exclusão", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
}
