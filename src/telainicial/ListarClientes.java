    private void carregarDadosClientes() {
        // Obtém o modelo da sua JTable (jtableClientes)
        DefaultTableModel model = (DefaultTableModel) jtableClientes.getModel();
        model.setRowCount(0); // Limpa as linhas existentes para um novo carregamento

        try {
            // 1. Busca a lista de clientes usando o ClienteDAO
            List<Cliente> clientes = clienteDAO.listarTodos();

            if (clientes.isEmpty()) {
                 JOptionPane.showMessageDialog(this, "Nenhum cliente encontrado no banco de dados.", 
                                              "Aviso", JOptionPane.INFORMATION_MESSAGE);
                 return; 
            }

            // 2. Adiciona cada cliente como uma nova linha
            for (Cliente cliente : clientes) {
                model.addRow(new Object[]{
                    cliente.getCpf(),
                    cliente.getNome(),
                    cliente.getTelefone(),
                    cliente.getEmail()
                });
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Erro ao carregar dados do banco. Verifique a Conexão e o Driver JDBC: " + ex.getMessage(), 
                "Erro de BD", JOptionPane.ERROR_MESSAGE);
        }
    }
    
